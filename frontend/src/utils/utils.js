import axios from "axios";

export const CONTRACT_ADDRESS= process.env.NEXT_PUBLIC_CONTRACT_ADDRESS
export const WEB3STORAGE_TOKEN = process.env.NEXT_PUBLIC_WEB3STORAGE_TOKEN
const POLYGONSCAN_API = process.env.NEXT_PUBLIC_POLYGONSCAN_API


/**
 * コントラクトのABIを取得
 * @returns {string} abi
 */
export const getABI = async () => {
  try {
    const res = await axios.get(
      `https://${POLYGONSCAN_API}/api?module=contract&action=getabi&address=${CONTRACT_ADDRESS}`
    );
    const data = res.data;
    return data.result;
  } catch (error) {
    console.log("axios error:", error);
  }
};

